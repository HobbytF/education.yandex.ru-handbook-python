def min(a, b):
    if a > b:
        return b
    else:
        return a
        

r = int(input())
n = r // 2 + r % 2
p = n
razm = 0
while p != 0:
    p //= 10
    razm += 1
for i in range(1, r + 1):
    s = ''
    for j in range(1, r + 1):
        s += str(min(abs(abs(i - n) - n), abs(abs(j - n) - n))).center(razm) + ' '
    print(s)



