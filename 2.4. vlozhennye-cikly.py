def nod(x, y):
    if x < y:
        x, y = y, x
    while x % y != 0:
        x, y = y, x % y
    return y
    
    
n = int(input())
nd = 1
for i in range(n):
    x = int(input())
    if i == 0:
        nd = nod(x, x)
    else:
        nd = nod(x, nd)
print(nd)


