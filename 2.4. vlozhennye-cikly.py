m = int(input())
p = n * m
razm = 0
while p != 0:
    p //= 10
    razm += 1
razm += 2
if razm % 2 == 0:
    razm += 1
for i in range(1, n + 1):
    s = ''
    for j in range(1, m + 1):
        s1 = str(i * j)
        k = 1
        while len(s1) < razm:
            if k % 2 == 0:
                s1 = ' ' + s1
            else:
                s1 = s1 + ' '
            k += 1
        s = s + s1 + '|'
    print(s[:-2])
    if i < n:
        print('-' * (razm * m + (m - 1)))


