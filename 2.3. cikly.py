def simple(n):
    f = True
    if n > 1:
        for i in range(3, n - 1, 2):
            if n % i == 0:
                f = False
    else:
        f = False
    return f


n = int(input())    
s = ''
i = 2
while not simple(n):
    while not (simple(i) and n % i == 0):
        i += 1
    s = s + str(i) + ' * '
    n = n // i
s = s + str(n)
print(s)


